export function detectLanguage(): Lang {
  if (typeof navigator === "undefined") return "enUS";
  const full = navigator.language.toLowerCase();
  // Distinguish US from other English variants
  if (full === "en-us" || full === "en_us") return "enUS";
  const code = full.slice(0, 2) as Lang;
  return LANGUAGES.find((l) => l.code === code) ? code : "enUS";
}

